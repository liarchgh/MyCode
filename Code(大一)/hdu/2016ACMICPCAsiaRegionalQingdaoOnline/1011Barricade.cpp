#include <cstdio>
#include <cstring>
#include <algorithm>
#include <fstream>
#include <iostream>

using namespace std;

int m, n;

int main(){
	int t;
	scanf("%d", &t);
	while(t--){
		int a, b, c;
		scanf("%d%d", &n, &m);
		for(int i = 0; i < m; ++i){
			scanf("%d%d%d", &a, &b, &c);
		}
	}
	return 0;
}
